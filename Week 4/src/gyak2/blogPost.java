package gyak2;


public class blogPost {
   String authorName;
   String title;
   String text;
   int publicationDate;

   public blogPost(String authorName, String title, String text, int publicationDate) {
       this.authorName = authorName;
       this.title = title;
       this.text = text;
       this.publicationDate = publicationDate;
   }
}
